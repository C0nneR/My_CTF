def base64Decode(text):
    alphabet = base64_box
    bit_str = ""
    text_str = ""

    for char in text:
        if char in alphabet:
            bin_char = bin(alphabet.index(char)).lstrip("0b")
            bin_char = bin_char.zfill(6)
            bit_str += bin_char

    brackets = [bit_str[x: x + 8] for x in range(0, len(bit_str), 8)]

    for bracket in brackets:
        text_str += chr(int(bracket, 2))

    return text_str


